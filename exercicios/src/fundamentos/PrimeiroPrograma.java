package fundamentos;

/**
 * O JAVADOC String é basicamente um comentário de múltiplas linhas,
 * mas ele tem o intuito de explicar e referenciar coisas no código
 * como páginas, pessoas e até mesmo explicar algo
 * 
 * Exemplos:
 * @author rodrigoeduardo
 * @since JDK 1.0
 *
 */

public class PrimeiroPrograma {
	public static void main(String[] args) {
		
//		Um sentença de código termina com ;
		System.out.println("Hello World #001!!!");
		System.out.println("Hello world #002!!!");
		
		/*
		 * Podemos comentar um código usando //
		 * ou usando /* * /
		 * 
		 * Podemos usar o atalho CTRL + / também
		 */
	}
}
